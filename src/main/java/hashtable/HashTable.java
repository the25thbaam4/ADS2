package hashtable;

public class HashTable {
    private int TABLE_SIZE;
    private int size;
    private HashEntry[] table;
    private int primeSize;

    public int getTABLE_SIZE() {
        return TABLE_SIZE;
    }

    public int getSize() {
        return size;
    }

    public HashEntry[] getTable() {
        return table;
    }

    public int getPrimeSize() {
        return primeSize;
    }




}
